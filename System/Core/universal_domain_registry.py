def get_all_agent_names():
    """Returns a list of all possible agent names."""
    agents = []
    for domain, specializations in DOMAINS.items():
        for spec in specializations:
            agents.append(f"{domain}_{spec}_agent")
    return agents

def get_all_sentinel_names():
    """Returns a list of all possible sentinel names."""
    sentinels = []
    for domain in DOMAINS.keys():
        for sentinel_type in SENTINEL_TYPES:
            sentinels.append(f"{domain}_{sentinel_type}")
    return sentinels

def get_domain_count():
    """Returns statistics about the registry."""
    total_specs = sum(len(v) for v in DOMAINS.values())
    total_domains = len(DOMAINS)
    total_sentinels = total_domains * len(SENTINEL_TYPES)
    return {
        "domains": total_domains,
        "specializations": total_specs,
        "potential_agents": total_specs,
        "potential_sentinels": total_sentinels,
        "total": total_specs + total_sentinels
    }

